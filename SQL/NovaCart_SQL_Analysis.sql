-- Business Case #2 – Pareto Analysis (80/20 Rule)
/* Scenario
	I've heard that about 20% of customers generate around 80% of revenue. Can you verify whether that's true for our business?
*/
WITH REVENUE_PER_CUSTOMER AS 
(
SELECT 
C.CUSTOMER_NAME AS CUSTOMER_NAME,
ROUND(SUM(O.SALES),2) AS REVENUE
FROM ORDERS O
JOIN CUSTOMERS C
ON O.CUSTOMER_ID = C.CUSTOMER_ID
GROUP BY C.CUSTOMER_NAME
),

CUMMULATIVE_REVENUE AS 
(
SELECT 
CUSTOMER_NAME,
REVENUE,
SUM(REVENUE) OVER (ORDER BY REVENUE DESC ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS CUMMULATIVE_REVENUE
FROM REVENUE_PER_CUSTOMER
),

CUMMULATIVE_PERCENTAGE AS (
SELECT 
CUSTOMER_NAME,
REVENUE,
CUMMULATIVE_REVENUE,
ROUND((CUMMULATIVE_REVENUE / (SELECT SUM(SALES) FROM ORDERS) * 100),2) AS PARETO_PERCENTAGE
FROM CUMMULATIVE_REVENUE)

SELECT 
COUNT(*) AS NUM_OF_CUSTOMERS
FROM CUMMULATIVE_PERCENTAGE
WHERE PARETO_PERCENTAGE <= 80;

/* 
Instead of 20% of customers contributing 80% of revenue, almost 60% of customers are needed to reach 80% of the revenue.

What does this tell us?
Revenue is fairly distributed among customers.
The business is not heavily dependent on a small group of customers.
Losing one or two customers would have less impact than in a business where revenue is concentrated among a few key customers.
Marketing and retention efforts should target a broader customer base rather than focusing only on a handful of high-value customers.
*/

-- Business Case #3 – RFM Analysis
/* Scenario
"We want to identify our best customers and customers who are at risk of leaving.

Please segment all customers based on:
How recently they purchased
How frequently they purchase
How much money they spend."
*/
WITH RFM_CAL AS 
(
SELECT 
C.CUSTOMER_NAME AS CUSTOMER,
MAX(O.ORDER_DATE) AS LAST_ORDER_DATE,
DATEDIFF((SELECT MAX(ORDER_DATE) FROM ORDERS), MAX(O.ORDER_DATE)) AS RECENCY,  #DATEDIFF(DATASET LAST ORDER DATE, CUSTOMER LAST ORDER DATE)
COUNT(*) AS FREQUENCY, #NUMBER OF ORDERS
SUM(O.SALES) AS MONETARY
FROM CUSTOMERS C
JOIN ORDERS O 
ON C.CUSTOMER_ID = O.CUSTOMER_ID
GROUP BY C.CUSTOMER_NAME),

RFM_SCORE AS 
(
SELECT 
CUSTOMER,
RECENCY,
FREQUENCY,
MONETARY,
NTILE(5) OVER (ORDER BY RECENCY DESC) AS R,
NTILE(5) OVER (ORDER BY FREQUENCY ASC) AS F,
NTILE(5) OVER (ORDER BY MONETARY ASC) AS M
FROM RFM_CAL
)

#CUSTOMER SEGMENTATION
SELECT 
CUSTOMER,
R,
F,
M,
CASE 
	WHEN R>=4 AND F>=4 AND M>= 4 THEN 'CHAMPION CUSTOMER'
    WHEN R>=4 AND F>=4 THEN 'LOYAL CUSTOMER'
    WHEN R>=4 THEN 'RECENT CUSTOMER'
    WHEN M>=4 THEN 'BIG SPENDER CUSTOMER' 
    WHEN R<=2 AND F<=2  THEN 'AT RISK CUSTOMER'
    ELSE 'REGULAR CUSTOMER'
END AS CUSTOMER_SEGMENT
FROM  RFM_SCORE
;

-- Inventory Optimization

-- We've got thousands of products. I don't know which ones deserve my attention. Can you help?
-- Which products are our fastest-moving products?
SELECT 
P.PRODUCT_ID AS PRODUCT_ID,
P.PRODUCT_NAME AS PRODUCT_NAME,
P.CATEGORY AS CATEGORY,
SUM(O.QUANTITY) AS TOTAL_QUANTITY_SOLD 
FROM PRODUCTS P 
JOIN ORDERS O 
ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY P.PRODUCT_ID,P.PRODUCT_NAME,P.CATEGORY
ORDER BY TOTAL_QUANTITY_SOLD DESC
;

