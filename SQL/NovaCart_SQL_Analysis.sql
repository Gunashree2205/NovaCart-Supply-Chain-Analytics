-- Monetary Values
SELECT 
COUNT(*) AS NUM_OF_ORDERS,
ROUND(SUM(SALES),2) AS REVENUE_TOTAL,
ROUND(SUM(PROFIT),2) AS TOTAL_PROFIT,
ROUND(AVG(SALES),2) AS AVERAGE_ORDER_VALUE
FROM ORDERS;

-- Top 5 States by Total_Revuene
SELECT 
C.STATE AS STATE,
ROUND(SUM(O.SALES),2) AS TOTAL_REVENUE
FROM ORDERS O
JOIN CUSTOMERS C
ON O.CUSTOMER_ID = C.CUSTOMER_ID
GROUP BY C.STATE
ORDER BY TOTAL_REVENUE DESC
LIMIT 5;

-- Which product categories generate the highest revenue and profit?
SELECT 
P.CATEGORY AS CATEGORY,
ROUND(SUM(O.SALES),2) AS TOTAL_REVENUE,
ROUND(SUM(O.PROFIT),2) AS TOTAL_PROFIT
FROM ORDERS O
JOIN PRODUCTS P
ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.CATEGORY
ORDER BY 2 DESC,3 DESC
;

-- Which warehouses are experiencing the most late deliveries?
SELECT 
W.WAREHOUSE_NAME AS WAREHOUSE,
COUNT(*) AS LATE_DELIVERIES
FROM ORDERS O
JOIN WAREHOUSES W
ON O.WAREHOUSE_ID = W.WAREHOUSE_ID
WHERE O.DELIVERY_STATUS = "Late"
GROUP BY WAREHOUSE
ORDER BY 2 DESC
;

-- average delivery time by shipping mode
SELECT 
O.SHIPPING_MODE AS SHIPPING_MODE,
ROUND(AVG(L.ACTUAL_DELIVERY_DAYS),2) AS AVERAGE_DELIVERY_DAYS
FROM ORDERS O 
JOIN LOGISTICS L
ON O.ORDER_ID = L.ORDER_ID
GROUP BY O.SHIPPING_MODE
ORDER BY AVERAGE_DELIVERY_DAYS ASC;

-- average delivery time by REGION
SELECT 
C.REGION AS REGION,
ROUND(AVG(L.ACTUAL_DELIVERY_DAYS),2) AS AVERAGE_DELIVERY_DAYS
FROM ORDERS O 
JOIN CUSTOMERS C 
ON O.CUSTOMER_ID = C.CUSTOMER_ID
JOIN LOGISTICS L ON O.ORDER_ID = L.ORDER_ID
GROUP BY C.REGION
ORDER BY AVERAGE_DELIVERY_DAYS ASC;

-- On-Time Delivery Performance
SELECT * FROM ORDERS;
SELECT 
DELIVERY_STATUS,
COUNT(*) AS TOTAL_ORDERS,
ROUND((COUNT(*) / (SELECT COUNT(*) FROM ORDERS))*100,2) AS ON_TIME_DELIVER_PERFORMANCE
FROM ORDERS
GROUP BY  DELIVERY_STATUS;

-- Find the return rate (%) for each product category.
SELECT 
P.CATEGORY AS CATEGORY,
COUNT(O.PRODUCT_ID) AS TOTAL_ORDERS,
COUNT(R.RETURN_ID) AS TOTAL_RETURNS,
ROUND((COUNT(R.RETURN_ID)/COUNT(O.ORDER_ID)) *100, 2) AS 'RETURN RATE %'
FROM ORDERS O 
JOIN PRODUCTS P ON O.PRODUCT_ID = P.PRODUCT_ID
LEFT JOIN RETURNS R ON R.ORDER_ID = O.ORDER_ID
GROUP BY P.CATEGORY
;

-- Which shipping mode is the fastest and which one is the most expensive?
SELECT 
O.SHIPPING_MODE AS SHIPPING_MODE,
COUNT(*) AS TOTAL_ORDERS,
ROUND(AVG(SHIPPING_COST),2) AS AVG_SHIPPING_COST,
ROUND(AVG(ACTUAL_DELIVERY_DAYS),2) AS AVG_DELIVER_DAYS
FROM ORDERS O
JOIN LOGISTICS L
ON O.ORDER_ID = L.ORDER_ID
GROUP BY O.SHIPPING_MODE
ORDER BY AVG_DELIVER_DAYS 
;
 
