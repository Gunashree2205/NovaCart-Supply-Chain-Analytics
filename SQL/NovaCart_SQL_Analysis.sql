-- Which warehouse generates the highest revenue?
WITH REVENUE_CTE AS (
SELECT 
W.WAREHOUSE_ID AS WAREHOUSE_ID,
W.WAREHOUSE_NAME AS WAREHOUSE_NAME,
W.REGION AS REGION,
ROUND(SUM(O.SALES),2) AS REVENUE
FROM WAREHOUSES W
JOIN ORDERS O
ON W.WAREHOUSE_ID = O.WAREHOUSE_ID
GROUP BY W.WAREHOUSE_ID,
W.WAREHOUSE_NAME,
W.REGION
),

-- Which warehouse processes the most orders?	
MOST_ORDERS_CTE AS (
SELECT 
W.WAREHOUSE_ID AS WAREHOUSE_ID,
W.WAREHOUSE_NAME AS WAREHOUSE_NAME,
W.REGION AS REGION,
COUNT(*) AS MOST_ORDERS
FROM WAREHOUSES W
JOIN ORDERS O
ON W.WAREHOUSE_ID = O.WAREHOUSE_ID
GROUP BY W.WAREHOUSE_ID,
W.WAREHOUSE_NAME,
W.REGION
),


-- Which warehouse is the most profitable?	
PROFIT_CTE AS (
SELECT 
W.WAREHOUSE_ID AS WAREHOUSE_ID,
W.WAREHOUSE_NAME AS WAREHOUSE_NAME,
W.REGION AS REGION,
ROUND(SUM(O.PROFIT),2) AS PROFIT
FROM WAREHOUSES W
JOIN ORDERS O
ON W.WAREHOUSE_ID = O.WAREHOUSE_ID
GROUP BY W.WAREHOUSE_ID,
W.WAREHOUSE_NAME,
W.REGION
),


-- Which warehouse has the highest return rate?
RETURN_RATE_CTE AS (
SELECT 
W.WAREHOUSE_ID AS WAREHOUSE_ID,
W.WAREHOUSE_NAME AS WAREHOUSE_NAME,
W.REGION AS REGION,
ROUND((COUNT(R.RETURN_ID) / COUNT(DISTINCT O.ORDER_ID)) * 100,2) AS RETURN_RATE
FROM ORDERS O
JOIN WAREHOUSES W ON O.WAREHOUSE_ID = W.WAREHOUSE_ID
LEFT JOIN RETURNS R ON O.ORDER_ID = R.ORDER_ID
GROUP BY W.WAREHOUSE_ID,
W.WAREHOUSE_NAME,
W.REGION
),

-- Which warehouse has the fastest delivery performance?
ON_TIME_DELIVERY_RATE_CTE AS (
SELECT 
W.WAREHOUSE_ID AS WAREHOUSE_ID,
W.WAREHOUSE_NAME AS WAREHOUSE_NAME,
W.REGION AS REGION,
ROUND((COUNT(CASE WHEN O.DELIVERY_STATUS = 'ON TIME' THEN DELIVERY_STATUS END) / COUNT(O.ORDER_ID) )*100,2) AS ON_TIME_DELIVERY_RATE
FROM ORDERS O
JOIN WAREHOUSES W ON O.WAREHOUSE_ID = W.WAREHOUSE_ID
GROUP BY W.WAREHOUSE_ID,
W.WAREHOUSE_NAME,
W.REGION
)

-- Warehouse Performance Scorecard

SELECT 
RC.WAREHOUSE_ID AS WAREHOUSE_ID,
RC.WAREHOUSE_NAME AS WAREHOUSE_NAME,
RC.REGION AS REGION,
RC.REVENUE AS REVENUE,
MOC.MOST_ORDERS AS MOST_ORDERS,
PC.PROFIT AS PROFIT,
RRC.RETURN_RATE AS RETURN_RATE,
OTDRC.ON_TIME_DELIVERY_RATE AS ON_TIME_DELIVERY_RATE
FROM REVENUE_CTE RC
JOIN MOST_ORDERS_CTE MOC ON RC.WAREHOUSE_ID = MOC.WAREHOUSE_ID
JOIN PROFIT_CTE PC ON RC.WAREHOUSE_ID = PC.WAREHOUSE_ID
JOIN RETURN_RATE_CTE RRC ON RC.WAREHOUSE_ID = RRC.WAREHOUSE_ID
JOIN ON_TIME_DELIVERY_RATE_CTE OTDRC ON RC.WAREHOUSE_ID = OTDRC.WAREHOUSE_ID
ORDER BY WAREHOUSE_ID ASC
;

-- Profitability Analysis
-- Show me the products that are actually losing us money.
SELECT 
P.PRODUCT_ID AS PRODUCT_ID,
P.PRODUCT_NAME AS PRODUCT_NAME,
P.CATEGORY AS CATEGORY,
ROUND(SUM(O.SALES),2) AS TOTAL_REVENUE,
ROUND(SUM(O.PROFIT),2) AS TOTAL_PROFIT
FROM PRODUCTS P
JOIN ORDERS O 
ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY PRODUCT_ID,
PRODUCT_NAME,
CATEGORY
HAVING TOTAL_PROFIT < 0
ORDER BY TOTAL_PROFIT DESC;

-- I don't want to know which category earns the most revenue.I want to know which category is the most profitable.
SELECT 
P.CATEGORY AS CATEGORY,
ROUND(SUM(O.SALES),2) AS TOTAL_REVENUE,
ROUND(SUM(O.PROFIT),2) AS TOTAL_PROFIT,
ROUND((SUM(O.PROFIT)/SUM(O.SALES)) * 100,2) AS PROFIT_MARGIN
FROM PRODUCTS P
JOIN ORDERS O 
ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY CATEGORY
ORDER BY PROFIT_MARGIN DESC;

-- Find products that have High Revenue but Low Profit
WITH PRODUCT_CONTRIBUTION AS (
SELECT P.PRODUCT_ID AS PRODUCT_ID,
P.PRODUCT_NAME AS PRODUCT_NAME,
ROUND(SUM(O.SALES),2) AS TOTAL_REVENUE,
ROUND(SUM(O.PROFIT),2) AS TOTAL_PROFIT,
ROUND((SUM(O.PROFIT)/SUM(O.SALES)) * 100,2) AS PROFIT_MARGIN
FROM PRODUCTS P
JOIN ORDERS O 
ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY PRODUCT_ID,
PRODUCT_NAME),

PRODUCT_QUARTILES AS 
(
SELECT
PRODUCT_ID,
PRODUCT_NAME,
TOTAL_REVENUE,
TOTAL_PROFIT,
PROFIT_MARGIN,
NTILE(4) OVER (ORDER BY TOTAL_REVENUE DESC) AS HIGH_REVENUE,
NTILE(4) OVER (ORDER BY TOTAL_PROFIT ASC) AS LOW_PROFIT
FROM PRODUCT_CONTRIBUTION
)

SELECT 
*
FROM PRODUCT_QUARTILES
WHERE HIGH_REVENUE = 1 AND LOW_PROFIT = 1
;

