SELECT 
P.PRODUCT_ID AS PRODUCT_ID,
P.PRODUCT_NAME AS PRODUCT_NAME,
P.CATEGORY AS CATEGORY,
SUM(O.QUANTITY) AS TOTAL_QUANTITY_SOLD 
FROM PRODUCTS P 
JOIN ORDERS O 
ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY P.PRODUCT_ID,P.PRODUCT_NAME,P.CATEGORY
ORDER BY TOTAL_QUANTITY_SOLD ASC
LIMIT 10
;

-- "I don't just want the fastest-selling products. I want to know which products contribute the most to our revenue so that we can prioritize inventory investment."
-- ABC INVENTORY CLASSIFICATION
WITH REVENUE_CONTRIBUTION AS (
SELECT 
P.PRODUCT_ID AS PRODUCT_ID,
P.PRODUCT_NAME AS PRODUCT_NAME,
P.CATEGORY AS CATEGORY,
ROUND(SUM(O.SALES),2) AS TOTAL_REVENUE
FROM PRODUCTS P 
JOIN ORDERS O 
ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY P.PRODUCT_ID,P.PRODUCT_NAME,P.CATEGORY
),

REVENUE_PERCENTAGE AS (
SELECT 
PRODUCT_ID,
PRODUCT_NAME,
CATEGORY,
TOTAL_REVENUE,
 ROUND(( TOTAL_REVENUE/ SUM(TOTAL_REVENUE) OVER () )* 100,2)  AS REVENUE_PERC
FROM REVENUE_CONTRIBUTION
),

CUMMULATIVE_REVENUE_PERC AS (
SELECT 
PRODUCT_ID,
PRODUCT_NAME,
CATEGORY,
TOTAL_REVENUE,
REVENUE_PERC,
SUM(REVENUE_PERC) OVER (ORDER BY TOTAL_REVENUE DESC ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) AS CUMMULATIVE_REVENUE_PERCENATGE
FROM REVENUE_PERCENTAGE
)

SELECT *,
CASE 
	WHEN CUMMULATIVE_REVENUE_PERCENATGE <= 80 THEN 'A'   #TOP 80% REVENUE CONTRIBUTION BY PRODUCTS
    WHEN CUMMULATIVE_REVENUE_PERCENATGE > 80 AND CUMMULATIVE_REVENUE_PERCENATGE <= 95 THEN 'B' #REST 15% REVENUE CONTRIBUTION BY PRODUCTS
    ELSE 'C' #REST 5% REVENUE CONTRIBUTION BY PRODUCTS
    END AS ABC_CATEGORY
    FROM CUMMULATIVE_REVENUE_PERC ;

